void print(const char* str){
    static unsigned char x = 0, y = 0;
    unsigned short attrib = 0x07;
    volatile unsigned short* vidmem;
    uint32_t *fb_ptr = fb->address; 

    for(int i = 0; str[i] != '\0'; i++) {
        vidmem = fb_ptr + (y * 80 + x);
        switch (str[i])
        {
            case '\b':
                vidmem = fb_ptr + (y * 80 + x);
                *vidmem = ' ' | (attrib << 8);
                vidmem--;
                *vidmem = '_' | (attrib << 8);
                x--;
                break;
            case '\n':
                *vidmem = ' ' | (attrib << 8);
                y++;
                x = 0;
                break;
            case '\v':
                for(y=0; y<25; y++) {
                    for(x=0; x<80; x++) {
                        vidmem = fb_ptr + (y * 80 + x);
                        *vidmem = 0x00;
                    }
                }
                x=0;
                y=0;
                break;
            default:
                char c = str[i];
                const uint8_t* bitmap = get_bitmap_for_char(c);
                for(int yy = 0; y < 13; y++) {
                    for(int xx = 0; x < 8; x++) {
                        uint8_t pixel = bitmap[yy * 8 + xx];
                        int fbx = x + 8;
                        int fby = y + 8;
                        uint32_t *fb_pxl = (uint32_t*)fb_ptr + fby * fb->pitch + fbx*4;
                        *fb_pxl = pixel;//get_pixel_color(pixel);
                    }
                }
                x += 8;
                break;
        }
        if(x >= 80) {
            x = 0;
            y++;
        }
        if (y >= 25) {
			
			unsigned short scroll_temp;

			for (y = 1; y < 25; y++) {	
				for (x = 0; x < 80; x++) {
					
					vidmem = fb_ptr + (80*y+x);
					scroll_temp = *vidmem;
						
					vidmem -= 80;
					*vidmem = scroll_temp;
					
					if (y == 24) {
						
						vidmem = fb_ptr + (1920+x);
						*vidmem = ' ' | (attrib << 8);
					}
				}
			}
			x = 0;
			y = 24;
		}
    }
}

